package main

import "fmt"

func main() {
	// INTEGERS - INT8, INT16 INT32 INT64
	var age int = 20
	fmt.Println(age)


	//STRINGS
	var name string = "Browny"
	fmt.Println(name)

	//BOOLEAN
	var isActive bool = false
	var isOpened bool = true
	fmt.Println(isActive, isOpened)

	//FLOATS - FLOAT32, FLOAT64
	var price float32 = 5.99
	fmt.Println(price)
}